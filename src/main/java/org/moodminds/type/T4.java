package org.moodminds.type;

/**
 * Generic object with four type parameters. Can be useful to transfer types when, for example,
 * it cannot be done with regular .class literal e.g. {@code Iterable<String>.class}.
 *
 * @param <V1> the type of parameter 1
 * @param <V2> the type of parameter 2
 * @param <V3> the type of parameter 3
 * @param <V4> the type of parameter 4
 */
public class T4<V1, V2, V3, V4> {}
